// && -- And
// if both are true -- then only its true otherwise false

// || -- Or
//if both are false -- then only it is false, otherwise it's true
console.log("***** Logical AND *****");

console.log(true && true);
console.log(true && false);
console.log(false && true);
console.log(false && false);

console.log("***** Logical OR *****");
console.log(true || true);
console.log(true || false);
console.log(false || true);
console.log(false || false);

console.log("***** Logical NOT *****");
console.log(!true);
console.log(!false);

console.log("*****Combination of Logical & Relational *****");
console.log(20>10 && 10<5);
console.log(20>10 || 10<5);
console.log(!(20>10));

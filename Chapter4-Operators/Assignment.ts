// Assignment with shorthand notation
// += , -= , *= , /= , %= , **=

let n1 = 10;
let n2 = 5;

n1 += 10; // num1 = num1 + 10; n1 = 20;
console.log(n1);

n2 -= 3; // num2 = num2 - 2;
console.log(n2); // n2 = 2;

n1 *= n2; // num1 = num1 * num2; n1 = 20 *2
console.log(n1);

n2 /= 2; // num2 = num2/2; num2 = 2/2 -- 1
console.log(n2);

n2 **=n1; //num2 = num2 ** num1 
console.log(n2);

n1 %= n2; //num1 = num1/num2
console.log(n1);

